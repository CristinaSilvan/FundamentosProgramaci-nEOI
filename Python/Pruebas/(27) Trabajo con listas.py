# Trabajando con listas

#   Declaración e impresión
A = [1,2,3,4]
print(A)

# Suma de listas
B = [5,6,7,8,9,10]
print(A+B)

# Listas de datos homogéneos
C = ["Hola", 5, -6.0, 'c']
print(C)

# Listas de listas
L = [A, B, C]
print(L)

# Consultar valores particulares de una lista
print(A[0])
print(L[2][3])
